#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xFE,0x9A,0xA1,0x50,0xC7,0xD9,0x05,0x5B,0x20,0xF1,0xAA,0x86,
		0xAC,0xAF,0x49,0x4C,0x09,0x9F,0xF7,0x8D,0x9F,0x6E,0x5F,0x75,
		0x71,0x27,0x8D,0xBF,0x78,0x49,0x35,0x52,0x6E,0xF4,0x65,0x32,
		0xCD,0x9E,0x62,0xA2,0x10,0x23,0xAF,0x61,0x90,0x86,0x00,0x14,
		0x76,0x89,0x22,0x2C,0xC6,0xA2,0x52,0xDF,0xE9,0xB1,0xEB,0x16,
		0x8B,0xFB,0x15,0xFB,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
